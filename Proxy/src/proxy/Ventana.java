    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     
      String Opcion = (String) jComboBox1.getSelectedItem(); 
        
        if(Opcion.equals("Depositar")){
            int numero = Integer.parseInt( this.jTextField2.getText() );
            c.Depositar(numero);
            this.jTextField2.setText("");
        }else if(Opcion.equals("Retirar")){
            int numero = Integer.parseInt( this.jTextField2.getText() );
            c.retirar(numero);
            this.jTextField2.setText("");
        }else if(Opcion.equals("Ver Cuenta"))
            this.jTextField2.setText(c.ConsutarSaldo());
    }//GEN-LAST:event_jButton2ActionPerformed

  
